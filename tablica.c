#include <stdio.h> // Подключение стандартной библиотеки для ввода-вывода

int main() {
    // Объявляем константу размера таблицы (от 1 до 9)
    const int size = 9;

    // Выводим верхнюю строку с заголовками колонок
    printf("    "); // Отступ слева для выравнивания таблицы
    for (int i = 1; i <= size; i++) {
        // Выводим заголовки колонок с шириной 3 символа для равномерности
        printf("%3d", i);
    }
    printf("\n"); // Переход на новую строку после заголовков

    // Выводим разделительную линию под заголовками
    printf("   +"); // Начинаем линию с символов для отделения заголовков
    for (int i = 1; i <= size; i++) {
        printf("---"); // Каждая часть линии занимает 3 символа
    }
    printf("\n"); // Переход на новую строку после линии

    // Основной цикл по строкам таблицы (от 1 до size)
    for (int row = 1; row <= size; row++) {
        // Выводим номер текущей строки слева, с шириной 2 символа, и вертикальной чертой
        printf("%2d |", row);
        // Внутренний цикл по столбцам (от 1 до size)
        for (int col = 1; col <= size; col++) {
            int product = row * col; // Вычисляем произведение текущей строки и столбца
            // Выводим произведение с шириной 3 символа для выравнивания по колонкам
            printf("%3d", product);
        }
        printf("\n"); // Переход на новую строку после каждой строки таблицы
    }

    return 0; // Возвращаем из функции main
}
