#include <stdio.h> // Подключение библиотеки для ввода-вывода

int main() {
    int X; // Объявляем переменную для количества уровней ёлочки
    printf("Введите количество уровней ёлочки (X): "); // Просим ввести число уровней
    scanf("%d", &X); // Считываем введенное число и сохраняем в переменную X

    for (int level = 1; level <= X; level++) {    // Цикл по уровням ёлочки, от 1 до X включительно
        int spaces = X - level; // Вычисляем количество пробелов перед звездочками для центрирования
        int stars = 1 + 2 * (level - 1); // Вычисляем количество звезд на текущем уровне

        for (int i = 0; i < spaces; i++) { // Цикл для вывода пробелов перед звездочками
            printf(" "); // Выводит один пробел
        }
      
        for (int i = 0; i < stars; i++) {  // Цикл для вывода звезд на текущем уровне
            printf("*"); // Выводит одну звездочку
        }
        printf("\n"); // Переход на новую строку после каждого уровня
    }

    // Построение ствола ёлочки, высотой равной X - 1
    int trunk_height = X - 1; // Высота ствола
    int trunk_width = 1; // Ширина ствола — один символ

    for (int i = 0; i < trunk_height; i++) {  // Цикл для построения ствола
        int total_width = 1 + 2 * (X - 1); // Расчет ширины самой широкой части ёлки — нижнего уровня
        int spaces = (total_width - trunk_width) / 2;  // Расчет количества пробелов перед стволом для центрирования

        for (int j = 0; j < spaces; j++) { // Цикл для вывода пробелов перед стволом
            printf(" "); // Выводит один пробел
        }
        // Цикл для вывода символов ствола ('|')
        for (int j = 0; j < trunk_width; j++) {
            printf("|"); // Выводит символ ствола
        }
        printf("\n"); // Переход на новую строку после каждого уровня ствола
    }

    return 0; // Возвращаем из функции main, программа завершена успешно
}
